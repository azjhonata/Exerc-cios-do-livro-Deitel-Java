// e utilizando variaçãoes da instrução print de saída
package exibirnumeros1a4namesmalinha;

public class ExibirNumeros1a4NaMesmaLinha {
// O metodo principal inicia a execução do aplicativo Java

    public static void main(String[] args) {
        System.out.println("1 2 3 4"); // Imprime na tela numeros de 1 a 4
        System.out.print("1 "); //Imprime na tela o numero 1
        System.out.print("2 "); //Imprime na tela o numero 2
        System.out.print("3 "); //Imprime na tela o numero 3
        System.out.print("4 \n"); //Imprime na tela o numero 4 e pula uma linha
        System.out.printf("1 2 3 4 \n"); //Imprime na tela numeros de 1 a 4
        // e pula uma linha
    } // fim do método principal

} // Fim da classe ExibirNumeros1a4NaMesmaLinha
